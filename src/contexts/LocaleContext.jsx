const LocaleContext = createContext();

export const LocaleProvider = ({ children }) => {
  const [lang, setLang] = useState(() => {
    try {
      return localStorage.getItem('locale') || 'en';
    } catch (e) {
      return 'en';
    }
  });

  useEffect(() => {
    document.documentElement.lang = lang;
    // set dir for RTL languages (Pashto and Dari use RTL script)
    if (lang === 'ps' || lang === 'fa') {
      document.documentElement.dir = 'rtl';
    } else {
      document.documentElement.dir = 'ltr';
    }
    try { localStorage.setItem('locale', lang); } catch (e) {}
  }, [lang]);

  // theme support removed per user request

  const getNested = (obj, path) => {
    if (!obj || !path) return undefined;
    const parts = String(path).split('.');
    let cur = obj;
    for (const p of parts) {
      if (cur && Object.prototype.hasOwnProperty.call(cur, p)) {
        cur = cur[p];
      } else {
        return undefined;
      }
    }
    return cur;
  };

  const t = (key) => {
    if (!key) return '';
    const val = getNested(translations[lang], key);
    if (val !== undefined) return val;
    const fallback = getNested(translations.en, key);
    if (fallback !== undefined) return fallback;
    return key;
  };

  return (
    <LocaleContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LocaleContext.Provider>
  );
};

export const useLocale = () => useContext(LocaleContext);
